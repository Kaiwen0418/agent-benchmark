#!/usr/bin/env node
import { readFile, readdir, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const packageDir = dirname(scriptDir);
const appsDir = join(packageDir, "src/apps");
const runtimeDir = join(packageDir, "src/runtime");
const checkOnly = process.argv.includes("--check");

function toIdentifier(slug) {
  return slug
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((part, index) => {
      const normalized = part[0].toUpperCase() + part.slice(1);
      return index === 0 ? part[0].toLowerCase() + part.slice(1) : normalized;
    })
    .join("");
}

function toTypeIdentifier(slug) {
  return slug
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join("");
}

async function discoverApps() {
  const entries = await readdir(appsDir, { withFileTypes: true });
  const apps = [];
  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const slug = entry.name;
    const definitionPath = join(appsDir, slug, "definition.ts");
    const typesPath = join(appsDir, slug, "types.ts");
    const testSupportPath = join(appsDir, slug, "test-support.ts");
    const testDriverPath = join(appsDir, slug, "test-driver.mjs");
    if (!existsSync(definitionPath) || !existsSync(typesPath)) continue;
    if (!existsSync(testDriverPath)) {
      throw new Error(`${relative(packageDir, testDriverPath)} is required for lifecycle smoke.`);
    }

    const definitionSource = await readFile(definitionPath, "utf8");
    const definitionMatch = definitionSource.match(/export const ([A-Za-z0-9_]+)\s*:\s*HostedAppDefinition/);
    if (!definitionMatch) {
      throw new Error(`${relative(packageDir, definitionPath)} must export a HostedAppDefinition const.`);
    }
    const testSupportSource = existsSync(testSupportPath) ? await readFile(testSupportPath, "utf8") : "";
    const testSupportMatch = testSupportSource.match(/export const ([A-Za-z0-9_]+)\s*:\s*HostedAppTestSupport/);
    if (!testSupportMatch) {
      throw new Error(`${relative(packageDir, testSupportPath)} must export a HostedAppTestSupport const.`);
    }

    apps.push({
      slug,
      definitionExport: definitionMatch[1],
      testSupportExport: testSupportMatch[1],
      typeAlias: `${toTypeIdentifier(slug)}AppSessionState`,
    });
  }
  return apps.sort((left, right) => left.slug.localeCompare(right.slug));
}

function generatedHeader() {
  return "// Generated by scripts/generate-app-registry.mjs. Do not edit by hand.\n";
}

function renderAppTypes(apps) {
  const imports = apps
    .map((app) => `import type { AppSessionState as ${app.typeAlias} } from "../apps/${app.slug}/types.js";`)
    .join("\n");
  const union = apps.map((app) => `"${app.slug}"`).join(" | ");
  const mapEntries = apps.map((app) => `  "${app.slug}": ${app.typeAlias};`).join("\n");
  return `${generatedHeader()}${imports}\n\nexport type HostedAppId = ${union};\n\nexport type HostedAppStateById = {\n${mapEntries}\n};\n`;
}

function renderAppDefinitions(apps) {
  const imports = apps
    .map((app) => `import { ${app.definitionExport} } from "../apps/${app.slug}/definition.js";`)
    .join("\n");
  const entries = apps.map((app) => `  "${app.slug}": ${app.definitionExport},`).join("\n");
  return `${generatedHeader()}${imports}\nimport type { HostedAppDefinition } from "./app-definition.js";\nimport type { HostedAppId } from "./types.js";\n\nexport const hostedAppDefinitions = {\n${entries}\n} satisfies { [TApp in HostedAppId]: HostedAppDefinition<TApp> };\n`;
}

function renderTestSupport(apps) {
  const imports = apps
    .map((app) => `import { ${app.testSupportExport} } from "../apps/${app.slug}/test-support.js";`)
    .join("\n");
  const entries = apps.map((app) => `  "${app.slug}": ${app.testSupportExport},`).join("\n");
  return `${generatedHeader()}${imports}\nimport type { HostedAppTestSupport } from "./test-support.js";\nimport type { HostedAppId } from "./types.js";\n\nexport const hostedAppTestSupport = {\n${entries}\n} satisfies { [TApp in HostedAppId]: HostedAppTestSupport<TApp> };\n`;
}

async function writeGenerated(fileName, contents) {
  const target = join(runtimeDir, fileName);
  if (checkOnly) {
    const existing = await readFile(target, "utf8");
    if (existing !== contents) {
      throw new Error(`${relative(packageDir, target)} is stale. Run pnpm --filter hosted-sites generate-registry.`);
    }
    return;
  }
  await writeFile(target, contents);
}

const apps = await discoverApps();
if (apps.length === 0) {
  throw new Error("No hosted app definitions found.");
}
await writeGenerated("generated-app-types.ts", renderAppTypes(apps));
await writeGenerated("generated-app-definitions.ts", renderAppDefinitions(apps));
await writeGenerated("generated-test-support.ts", renderTestSupport(apps));

#!/usr/bin/env node
import { existsSync } from "node:fs";
import { readFile, readdir, writeFile } from "node:fs/promises";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const packageDir = dirname(dirname(fileURLToPath(import.meta.url)));
const appsDir = join(packageDir, "src/apps");
const target = join(packageDir, "src/generated-app-registry.ts");
const checkOnly = process.argv.includes("--check");

async function discoverApps() {
  const entries = await readdir(appsDir, { withFileTypes: true });
  const apps = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const definitionPath = join(appsDir, entry.name, "definition.ts");
    if (!existsSync(definitionPath)) {
      throw new Error(`${relative(packageDir, definitionPath)} is required.`);
    }
    const source = await readFile(definitionPath, "utf8");
    const match = source.match(/export const ([A-Za-z0-9_]+TestcaseDefinition)\s*=/);
    if (!match) {
      throw new Error(`${relative(packageDir, definitionPath)} must export a *TestcaseDefinition const.`);
    }
    apps.push({ slug: entry.name, exportName: match[1] });
  }

  return apps.sort((left, right) => left.slug.localeCompare(right.slug));
}

function render(apps) {
  const imports = apps
    .map((app) => `import { ${app.exportName} } from "./apps/${app.slug}/definition.js";`)
    .join("\n");
  const definitions = apps
    .map((app) => `  "${app.slug}": ${app.exportName},`)
    .join("\n");
  const schemas = apps
    .map((app) => `export const ${app.exportName.replace(/TestcaseDefinition$/, "SessionSchema")} = createHostedSessionSchema("${app.slug}", ${app.exportName}.questionVariantSchema);`)
    .join("\n");
  const schemaNames = apps
    .map((app) => app.exportName.replace(/TestcaseDefinition$/, "SessionSchema"))
    .join(",\n  ");

  return `// Generated by scripts/generate-app-registry.mjs. Do not edit by hand.
import { z } from "zod";
${imports}
import { createHostedSessionSchema } from "./app-definition.js";

export const hostedTestcaseApps = {
${definitions}
} as const;

${schemas}

export const hostedSessionDefinitionSchema = z.discriminatedUnion("app", [
  ${schemaNames},
]);

export function parseQuestionVariants(app: string, value: unknown) {
  const definition = hostedTestcaseApps[app as keyof typeof hostedTestcaseApps];
  if (!definition) {
    throw new Error(\`Unsupported hosted app \${app}.\`);
  }
  if (!Array.isArray(value)) {
    throw new Error("Hosted session is missing metadata.questionVariants.");
  }
  if (value.length < 2) {
    throw new Error("Hosted session must define at least two question variants.");
  }
  const variants = definition.questionVariantSchema.array().parse(value);
  if (new Set(variants.map((variant) => variant.id)).size !== variants.length) {
    throw new Error("Hosted session question variant ids must be unique.");
  }
  return variants;
}
`;
}

const generated = render(await discoverApps());
if (checkOnly) {
  if (!existsSync(target) || await readFile(target, "utf8") !== generated) {
    throw new Error("src/generated-app-registry.ts is stale. Run pnpm --filter @agentbench/test-cases generate-registry.");
  }
} else {
  await writeFile(target, generated);
}
